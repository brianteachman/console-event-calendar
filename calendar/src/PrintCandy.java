public class PrintCandy {
    protected static void print(String thing) {
        System.out.print(thing);
    }
    protected static void println(String thing) {
        System.out.println(thing);
    }
    protected static void println(char thing) {
        System.out.println(thing);
    }

    protected static void print(int num) {
        System.out.print(num);
    }
    protected static void print(char num) {
        System.out.print(num);
    }
    protected static void println(int num) {
        System.out.println(num);
    }

    protected static void println() {
        System.out.println();
    }

}
